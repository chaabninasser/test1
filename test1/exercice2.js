function replace(a) {
    var a;
    var j;
    var k;
    var h = 0;

    a = 1 || a = 0;
    T = [a, a, a, a, a, a, a, a, a];

    for (var i = 0; i <= T.length; i++) {
        if (T[i] = 0) {
            i = j;
            T[i] = 1;
            if (T[i] = T[i + 1]) {
                i++
            }
            else {
                i = k
            }
            h = k - j + 1;
        }
        if (h <= k - j + 1) {
            h = k - j + 1;
        }

    }
    return h

}